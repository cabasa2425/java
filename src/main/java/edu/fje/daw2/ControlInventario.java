/**
 * Interfaz para el control de inventario en la biblioteca.
 * Define métodos para actualizar y mostrar el inventario.
 */
package edu.fje.daw2;

public interface ControlInventario {

    /**
     * Actualiza el inventario del elemento.
     */
    void actualizarInventario();

    /**
     * Muestra la información del inventario del elemento.
     */
    void mostrarInventario();
}